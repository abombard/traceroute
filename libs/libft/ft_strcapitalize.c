char	*my_strcapitalize(char *str)
{
	int	x;

	if (str[0] >= 'a' && str[0] <= 'z')
	{
		str[0] = str[0] - 32;
	}
	x = 1;
	while (str[x])
	{
		if (str[x] >= '0' && str[x] <= '9')
		{
			x = x + 1;
		}
		if ((str[x - 1] == ' ' || str[x - 1] == '-' || str[x - 1] == '+') &&
			(str[x] >= 'a' && str[x] <= 'z'))
		{
			str[x] = str[x] - 32;
		}
		x = x + 1;
	}
	return (str);
}
